import { existsSync, readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { defineConfig } from 'astro/config';
import starlight from '@astrojs/starlight';

const __dirname = dirname(fileURLToPath(import.meta.url));
const VERSIONS_JSON = resolve(__dirname, 'src/generated/versions.json');

const skipAutoSitemap = {
  name: '@astrojs/sitemap',
  hooks: {},
};

// Read the manifest emitted by scripts/build-versioned-docs.mjs. The pre*
// hooks regenerate it before astro starts, so it should always be present.
// Failing fast here catches mis-wired build configs early instead of letting
// astro silently produce a sidebar that mixes versions.
function readVersions() {
  if (!existsSync(VERSIONS_JSON)) {
    throw new Error(
      `[docs] expected ${VERSIONS_JSON}; run scripts/build-versioned-docs.mjs first`,
    );
  }
  return JSON.parse(readFileSync(VERSIONS_JSON, 'utf8'));
}

const { versions } = readVersions();

// Each version gets its own autogenerated sidebar group keyed by the version
// slug. The route middleware (src/starlightRouteData.ts) reads the URL and
// promotes only the matching group's entries onto `starlightRoute.sidebar`,
// so a v0-4-0 page never sees latest's tree even though Starlight builds
// every group up front.
const versionSidebars = versions.map((v) => ({
  label: v.slug,
  collapsed: false,
  autogenerate: { directory: v.slug },
}));

export default defineConfig({
  site: 'https://chaos-maker-dev.github.io/chaos-maker',
  base: '/chaos-maker',
  integrations: [
    skipAutoSitemap,
    starlight({
      title: 'Chaos Maker',
      description: 'Frontend chaos engineering toolkit for testing resilience across Playwright, Cypress, WebdriverIO, and Puppeteer.',
      favicon: '/favicon.png',
      social: [
        { icon: 'github', label: 'GitHub', href: 'https://github.com/chaos-maker-dev/chaos-maker' },
      ],
      // Per-page `editUrl` is injected by scripts/build-versioned-docs.mjs:
      // archived versions get `editUrl: false`, latest/main get an explicit
      // URL pointing at `docs/content-source/<original-relative-path>`. A
      // single `editLink.baseUrl` here cannot strip the leading version
      // segment, so it would produce broken paths like
      // `docs/content-source/latest/...`.
      lastUpdated: true,
      sidebar: versionSidebars,
      components: {
        Header: './src/components/Header.astro',
      },
      routeMiddleware: ['./src/starlightRouteData.ts'],
    }),
  ],
});
